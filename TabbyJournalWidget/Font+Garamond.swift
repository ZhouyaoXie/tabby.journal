import SwiftUI

extension Font {
    static func garamond(size: CGFloat) -> Font {
        .custom("EBGaramond-Regular", size: size)
    }
    static func garamondBold(size: CGFloat) -> Font {
        .custom("EBGaramond-Bold", size: size)
    }
    static func garamondItalic(size: CGFloat) -> Font {
        .custom("EBGaramond-Italic", size: size)
    }
}
